package net.teamfps.ld30;

import java.awt.event.KeyEvent;

/**
 * @author Zekye
 *
 */
public class Camera {
	private int x;
	private int y;
	private int speed = 3;

	public Camera(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}
}
